    private void NewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewActionPerformed
        Connection con;
        Validation validate = new Validation(true);
        String newID="";
        
        try {
            con = DriverManager.getConnection(this.connectionUrl);
            
            CallableStatement cStmt = con.prepareCall("{call newid_proc()}");

            ResultSet result = cStmt.executeQuery();
            if(result.next())
            {
                newID = result.getString(1);
            }
            
            if(!this.validation()) return;
            
            cStmt = con.prepareCall("{call insert_proc(?,?,?,?,?,?,?,?,?,?,?)}");
            cStmt.setString(1, newID);
            cStmt.setString(2, this.CompanyName.getText());
            cStmt.setString(3, this.ContactName.getText());
            cStmt.setString(4, this.ContactTitle.getText());
            cStmt.setString(5, this.Adress.getText());
            cStmt.setString(6, this.City.getText());
            cStmt.setString(7, this.Region.getText());
            cStmt.setString(8, this.PostalCode.getText());
            cStmt.setString(9, this.Country.getText());
            cStmt.setString(10, this.Phone.getText());
            cStmt.setString(11, this.Fax.getText());
            cStmt.execute();
            
            cStmt = con.prepareCall("{call rownum(?,?)}");
            cStmt.setString(1, newID);
            cStmt.setInt(2, currOder);
                
            result = cStmt.executeQuery();
            
            this.fillTable(this.currOder);
            if(result.next())
            {
                Tabela.clearSelection();
                Tabela.setRowSelectionInterval(result.getInt(1)-1, result.getInt(1)-1);      
                Tabela.scrollRectToVisible(new Rectangle(Tabela.getCellRect(result.getInt(1), 0, true)));
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Problem with server connection", "Connection Error", 0);
            Logger.getLogger(MyWin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_NewActionPerformed

