    public void fillTable(int order) throws SQLException {
        currOder = order;
        String ID="";
        boolean selected=false;
        if(Tabela.getSelectedRow()>=0) {
            ID = (String)cm.getValueAt(Tabela.getSelectedRow(), 0);
            selected = true;
        }
        
        this.Tabela.repaint();
        cm = (CustomerModel) this.Tabela.getModel();
        cm.reset();
        try {
            Connection con = DriverManager.getConnection(this.connectionUrl);
            
            CallableStatement MainStatment = con.prepareCall("{call filltable(?)}");
            
            MainStatment.setInt(1, order);
            ResultSet result = MainStatment.executeQuery();
            
            while (result.next()) {
            	cm.addCustomer(new Customer(result.getString(1), result.getString(2), result.getString(3), 
                                            result.getString(4), result.getString(5), result.getString(6), 
                                            result.getString(7), result.getString(8), result.getString(9), 
                                            result.getString(10),result.getString(11)));
            }
            if(selected) {
                CallableStatement SelectStatment = con.prepareCall("{call rownum(?,?)}");
                SelectStatment.setString(1, ID);
                SelectStatment.setInt(2, order);
                
                result = SelectStatment.executeQuery();
                if(result.next()) {
                    Tabela.clearSelection();
                    Tabela.setRowSelectionInterval(result.getInt(1)-1, result.getInt(1)-1);
                    Tabela.scrollRectToVisible(new Rectangle(Tabela.getCellRect(result.getInt(1)-1, 0, true)));
                }               
            }
            
       con.close();
       } catch (SQLException ex) {
            Logger.getLogger(MyWin.class.getName()).log(Level.SEVERE, null, ex);
       }
    }        
