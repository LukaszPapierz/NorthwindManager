    public boolean validation() {
        Validation validate = new Validation(true);
        
            if(!validate.isAlphaNum(this.CompanyName.getText())) {
                validate.messages.addLast("Incorrect company name");
            }
            
            if(!validate.isAlphaNum(this.ContactTitle.getText())) {
                validate.messages.addLast("Incorrect contact title");
            }
            
            if(!validate.isAlphaNum(this.ContactName.getText())) {
                validate.messages.addLast("Incorrect contact name");
            }
            
            if(!validate.isAlphaNum(this.Adress.getText())) {
                validate.messages.addLast("Incorrect adress");
            }
            
            if(!validate.isLetters(this.Country.getText())) {
                validate.messages.addLast("Incorrect country");
            }
            
            if(!validate.isLetters(this.City.getText())) {
                validate.messages.addLast("Incorrect city");
            }
            
            if(!validate.isLetters(this.Region.getText())) {
                validate.messages.addLast("Incorrect region");
            }
            
            if(!validate.isAlphaNum(this.PostalCode.getText())) {
                validate.messages.addLast("Incorrect postal code");
            }
            
            if(!validate.isNum(this.Phone.getText())) {
                validate.messages.addLast("Incorrect phone number");
            }
            
            if(!validate.isNum(this.Fax.getText())) {
                validate.messages.addLast("Incorrect fax number");
            }           
            
            
            if(!validate.correct) {
                JOptionPane.showMessageDialog(null, validate.messages.getFirst(), validate.title, 0);
            }
            
            return validate.correct;
    }
    
